int		ft_count_if(char **tab, int (*f)(char *))
{
	int		i;
	int		n;

	n = 0;
	i = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			n += 1;
		i += 1;
	}
	return (n);
}
